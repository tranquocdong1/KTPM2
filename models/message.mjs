import mongoose from 'mongoose';
import { contactDBConnection } from '../config/connectDB.mjs';

const messageSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    subject: {
        type: String,
        required: true
    },
    message: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
});

export default contactDBConnection.model('Message', messageSchema);
